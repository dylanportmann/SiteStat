function unique(values: string[]): string[] {
  return Array.from(new Set(values));
}

function commandLegendText(entry: CommandEntry): string {
  return [entry.command, entry.example, ...(entry.basicEquivalentSteps ?? [])].join("\n");
}

export function legendKeysForCommand(entry: CommandEntry): string[] {
  const text = commandLegendText(entry);
  const inferred = detectionRules
    .filter(([, pattern]) => pattern.test(text))
    .map(([id]) => id);

  return unique([...(entry.legendKeys ?? []), ...inferred]).filter((id) => legendById.has(id));
}

export function legendEntriesForCommand(entry: CommandEntry): LegendEntry[] {
  return legendKeysForCommand(entry)
    .map((id) => legendById.get(id))
    .filter((entry): entry is LegendEntry => Boolean(entry));
}
